a, b, c = 10, 5, 2

print("Sum=",(a+b))                 # Addition
print("Difference=",(a-b))          # Subtraction
print("Product=",(a*b))             # Multiplication
print("Quotient=",(a/b))            # Quotient
print("Remainder=",(b%c))           # Remainder
print("Exponent=",(b**2))           # Exponent
print("Floor Division=",(b//c))     # Floor Division