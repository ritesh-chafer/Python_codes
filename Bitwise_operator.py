a,b = 60, 2                #a = 0011 1100 , b = 0000 0010

print(a&b)                 #Binary AND
print(a|b)                 #Binary OR
print(a^b)                 #Binary XOR
print(~b)                  #Binary 1's complement
print(a>>b)                #Binary Left shift
print(a<<b)                #Binary Right Shift
